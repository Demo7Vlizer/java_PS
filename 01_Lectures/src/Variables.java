public class Variables {
    public static void main(String[] args) {

        //-- Declaration
        int myNumber = 865;

        //-- Initialization
        int yourNumber;

//        System.out.println(yourNumber);  -> This will show an error, Because

        yourNumber = 865;

        System.out.println(yourNumber);

        //-- Removing.. or Replacing or Change..
        yourNumber =45;

        //----------------------------------------------

        System.out.println(yourNumber);

        boolean isVegetarian = true;

        System.out.println(isVegetarian);

        //----------------------------------------------

        float myFloat = 5;
        System.out.println(myFloat);

        float myAnotherFloat = 7.0f;
        System.out.println(myAnotherFloat);

        double myDouble = 8.93839;
        System.out.println(myDouble);

        //----------------------------------------------

        String myString = "Hello";
        System.out.println(myString);

        //----------------------------------------------

        char myCharacter = 'K';   //-- Here only we put only one character if we give more than one it'll show error
        System.out.println(myCharacter);

        //----------------------------------------------


    }
}
