public class Escape {
    public static void main(String[] args) {
        System.out.println("Hello \"There\" ");    //-- (Hello "There" ) _ If you want to show double string so you've to use backslash before
        System.out.println("Hello \\There\\ ");  //--(Hello \There\ ) _ In here you've to show backslash so you've to use double backslash
        System.out.println("Hello \nThere");     //-- For next line..
        System.out.println("Hello \tThere");      //-- (Hello 	There) _ It'll give you a space around tab space..
        System.out.println("Hello \bThere");      //-- HelloThere
        System.out.println("Hello\bThere");      //-- HellThere


    }
}
